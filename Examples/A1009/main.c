#include<stdio.h>

int main()
{
	int a = 5;
	long b = 9;
	
	int c = 0;
	short d = 1;
	
	printf("%d\n", ++a);
	printf("%d\n", --b);
	
	printf("%d\n", c++);
	printf("%d\n", d--);
		 
	return 0;
}
